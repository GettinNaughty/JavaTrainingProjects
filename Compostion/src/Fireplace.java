
public class Fireplace {
	private int width;
	private int height;
	private String logTypes;
	public Fireplace(int width, int height, String logTypes) {
		super();
		this.width = width;
		this.height = height;
		this.logTypes = logTypes;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public String getLogTypes() {
		return logTypes;
	}
	public void setLogTypes(String logTypes) {
		this.logTypes = logTypes;
	}
	
	
}
